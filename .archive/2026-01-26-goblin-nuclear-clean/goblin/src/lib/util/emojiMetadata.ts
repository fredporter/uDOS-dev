/**
 * Get all emoji codes that have both mono and color versions
 */
export function getMatchedEmojiCodes(): number[] {
  return EMOJI_CORE.filter((e) => e.hasMonoVersion && e.hasColorVersion).map(
    (e) => e.code
  );
}

/**
 * Get GitHub shortcode for an emoji code
 */
export function getGithubShortcode(code: number): string | undefined {
  return EMOJI_CORE.find((e) => e.code === code)?.githubShortcode;
}

/**
 * Get emoji code from GitHub shortcode
 */
export function getEmojiFromShortcode(shortcode: string): number | undefined {
  return EMOJI_CORE.find((e) => e.githubShortcode === shortcode)?.code;
}

/**
 * Get size hint for an emoji
 */
export function getSizeHint(code: number): number {
  return EMOJI_CORE.find((e) => e.code === code)?.sizeHint || 1.0;
}

/**
 * Get emojis by category
 */
export function getEmojisByCategory(category: EmojiCategory): EmojiMetadata[] {
  return EMOJI_CORE.filter((e) => e.category === category);
}

/**
 * Get all categories
 */
export function getAllCategories(): EmojiCategory[] {
  return Array.from(new Set(EMOJI_CORE.map((e) => e.category)));
}
